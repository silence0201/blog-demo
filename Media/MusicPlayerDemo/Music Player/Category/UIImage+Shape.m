/**
 *  返回主bundle的图片对象
 *
 */
+(UIImage *)imageFromMainBundleWithName:(NSString *)name{
    NSString *imagePath = [[NSBundle mainBundle] pathForResource:name ofType:nil];
    return [UIImage imageWithContentsOfFile:imagePath];
    
}


